4. Получите количество задач для каждого гнома, сгруппировав их по статусу. 
Мой запрос:
SELECT D.dwarf_id, D.name, T.status, COUNT(*) 
	FROM Dwarves D 
	JOIN Tasks T ON D.dwarf_id = T.assigned_to 
	GROUP BY D.dwarf_id, D.name, T.status;
Эталонный запрос:
SELECT assigned_to, status, COUNT(*) AS task_count
    FROM Tasks
    GROUP BY assigned_to, status;
Я сделал лишний join, чтобы вывести имя гнома. Если выводить только id, то можно сократить запрос.

