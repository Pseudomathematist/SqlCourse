1. Найдите все отряды, у которых нет лидера. 
Мой запрос:
SELECT S.squad_id, S.name 
	FROM Squads S 
	JOIN Dwarves D ON S.leader_id = D.dwarf_id 
	WHERE D.squad_id IS NULL OR D.squad_id <> S.squad_id;
Эталонный запрос:
SELECT *
    FROM Squads
    WHERE leader_id IS NULL;
Так как новый архитектор "оптимизировал" базу данных,
а у leader_id не указано, что поле может быть NULL, я подумал,
что он сломал базу, и отсутствие лидера означает, что у "лидера" нет отряда или он находится в другом отряде.

